#include "3-calc.h"
/**
 * op_add - function to add 2 integers
 * @a: first int
 * @b: second int
 * Return: sum of two numbers
 */
int op_add(int a, int b)
{
return (a + b);
}

/**
 * op_sub - function to find the difference of 2 integers
 * @a: first int
 * @b: second int
 * Return: difference of two numbers
 */
int op_sub(int a, int b)
{
return (a - b);
}

/**
 * op_mul - function to multiply 2 integers
 * @a: first int
 * @b: second int
 * Return: product of two numbers
 */
int op_mul(int a, int b)
{
return (a * b);
}

/**
 * op_div - function to find division of 2 integers
 * @a: first int
 * @b: second int
 * Return: (a / b)
 */
int op_div(int a, int b)
{
return (a / b);
}

/**
 * op_mod - function to get modulo a,b
 * @a: first int
 * @b: second int
 * Return: remainder of the division of numbers
 */
int op_mod(int a, int b)
{
return (a % b);
}
